std::vector<PointSample>
    LoadPoints(const std::string& filename) {
  std::vector<PointSample> points;
  std::ifstream in(filename);
  std::string line;
  while (std::getline(in, line)) {
    if (line.size() < 4) {
      continue;
    }
    std::istringstream iss(line);
    PointSample ps;
    iss >> ps.pos[0] >> ps.pos[1] >> ps.pos[2] >> ps.val;
    points.push_back(ps);
  }
  in.close();
  return points;
}

